public class ExamplePrimitives {
    public static void main(String[] args) {
        // Integers
        byte b = 32;
        short s = 23000;
        int i = 45000;
        long l = 12345678901L;

        // Floats
        float f = 3.14f;
        double d = 3.141592653589793;

        // Chars
        char c1 = 'a';
        char c2 = 45;

        // Booleans
        boolean b1 = true;
        boolean b2 = false;
    }
}