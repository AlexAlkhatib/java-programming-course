public class ExampleOperators {
    public static void main(String[] args) {
        // relational operators
        int small = 2;
        int big = 10;

        System.out.println(small == big); // False
        System.out.println(small != big); // True
        System.out.println(small >= big); // False
        System.out.println(small <= big); // True
        System.out.println(small > big); // False
        System.out.println(small < big); // True
    }
}